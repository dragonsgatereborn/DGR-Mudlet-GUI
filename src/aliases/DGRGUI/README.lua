Important
- Do not add or change anything inside the @PKGNAME@ package folders.
- EMCO updates overwrite package contents and will delete custom changes.
- Put your custom triggers/aliases/scripts outside @PKGNAME@, or in a separate custom package.

Install
- lua uninstallPackage("DGRGUI")
- lua installPackage("https://github.com/dragonsgatereborn/DGR-Mudlet-GUI/releases/latest/download/@PKGNAME@.mpackage")
- emco update (updates from GitHub)

GUI Aliases
- gui install
- gui uninstall
- gui update
- gui restart
- gui version

